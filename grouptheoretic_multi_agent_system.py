"""**Block 3: the Verifier itself**: Let's scope what it needs to do for the `COMMUTATOR` path first (`STRUCTURE_CONST` is simpler).

*What raw_expr parsing needs to extract*, given $[T_1, T_2] = iT_3 + iT_8$:
* Left side: a list of exactly 2 generator indices inside `[...] → (1, 2)`
* Right side: a SymPy-parseable expression in terms of `i (→ I)` and `T_k` symbols, which we then need to evaluate as "$T_3$ scaled by $i$, plus $T_8$ scaled by $i$" — i.e., a linear combination of the actual generator matrices, not literal symbols.

That last point is the crux of the design: the right-hand side isn't just a SymPy expression we symbolically simplify — it's a recipe for which generator matrices to combine and with what coefficients, which we then need to substitute against the real matrices from `su_n_generators(N)` and compare against the real computed commutator.
So the approach:
* Regex out the two LHS indices from `[T_a, T_b]` (or `[Ta, Tb]`) → handle both underscore and bareword via one regex.
* Take the RHS string, replace `T_k` / `Tk` tokens with placeholder SymPy symbols (`T1_sym`, `T2_sym`, ...), replace `i` with `I`, then `sympy.sympify()` the result into an expression in terms of those symbols.
* Extract the coefficient of each `Tk_sym` from the sympified expression (via `.coeff()` or by collecting), giving us a coefficient vector.
* Build the actual RHS matrix as `sum(coeff_k * T[k-1] for k in ...)` using the real generator matrices.
* Compute the actual LHS commutator `commutator(T[a-1], T[b-1])`.
* Compare via `simplify(lhs_matrix - rhs_matrix) == zeros(N,N)`.

**Goal:** a function that takes `raw_expr` (the string after the tag) and returns the actual SymPy matrices for LHS and RHS, ready for direct comparison — no LLM, no graph, just `string → matrices`.

A few design decisions:
* I'll split on `=` first (exactly one `=` expected — that's itself a thing to validate, fail loudly if not exactly `1`).
* LHS regex: `\[T_?(\d+)\s*,\s*T_?(\d+)\]` — handles both `T1`/`T_1` via the optional underscore.
* RHS: I'll tokenize `T_?(\d+)` occurrences first to find which indices appear, build a coefficient-extraction approach by substituting each `T_k` with a unique SymPy Symbol, `sympify` the whole RHS string, then use `.as_coefficients_dict()` or `.coeff(symbol)` per symbol found.
* **Critically**: I need `T` available as generator matrices, so this function needs the `T` list (or a `su_n_generators`-derived list) passed in — it shouldn't build its own. Keeps it composable with whatever algebra size the claim specifies.
"""

import re
from sympy import sympify, Symbol, I as sympy_I, zeros

def parse_commutator_expr(raw_expr: str, T: list) -> tuple:
    """
    Parse a string like '[T_1, T_2] = i*T_3 + i*T_8' into actual matrices,
    using the generator list T (0-indexed: T[0] = T1, T[1] = T2, ...).

    Returns (lhs_matrix, rhs_matrix) as SymPy Matrices, ready for direct
    comparison. Raises ValueError on any parsing failure.
    """
    sides = raw_expr.split("=")
    if len(sides) != 2:
        raise ValueError(f"Expected exactly one '=' in expression, got {len(sides)-1}: {raw_expr!r}")
    lhs_str, rhs_str = sides[0].strip(), sides[1].strip()

    # --- Parse LHS: [T_a, T_b] ---
    lhs_match = re.fullmatch(r"\[\s*T_?(\d+)\s*,\s*T_?(\d+)\s*\]", lhs_str)
    if not lhs_match:
        raise ValueError(f"Could not parse LHS as [Ta, Tb]: {lhs_str!r}")
    a, b = int(lhs_match.group(1)), int(lhs_match.group(2))

    n_gens = len(T)
    if not (1 <= a <= n_gens) or not (1 <= b <= n_gens):
        raise ValueError(f"LHS indices T{a}, T{b} out of range for {n_gens} generators.")

    lhs_matrix = T[a - 1] * T[b - 1] - T[b - 1] * T[a - 1]  # commutator

    # --- Parse RHS: linear combination of i, T_k, sqrt(3), numbers ---
    # Find every T_k / Tk token referenced on the RHS
    rhs_indices = sorted(set(int(m) for m in re.findall(r"T_?(\d+)", rhs_str)))
    if not rhs_indices:
        raise ValueError(f"No T_k terms found on RHS: {rhs_str!r}")
    for k in rhs_indices:
        if not (1 <= k <= n_gens):
            raise ValueError(f"RHS index T{k} out of range for {n_gens} generators.")

    # Substitute T_k / Tk -> unique placeholder symbols, then sympify
    placeholder_syms = {k: Symbol(f"__T{k}__") for k in rhs_indices}
    rhs_substituted = re.sub(
        r"T_?(\d+)",
        lambda m: f"__T{int(m.group(1))}__",
        rhs_str,
    )
    rhs_expr = sympify(rhs_substituted, locals={"i": sympy_I})

    # Extract coefficient of each placeholder symbol -> build RHS matrix
    rhs_matrix = zeros(T[0].shape[0], T[0].shape[1])
    for k, sym in placeholder_syms.items():
        coeff = rhs_expr.coeff(sym)
        rhs_matrix += coeff * T[k - 1]

    # Sanity check: does the expression fully decompose into known T_k terms?
    # (i.e. no leftover constant/non-T_k piece)
    reconstructed = sum(rhs_expr.coeff(sym) * sym for sym in placeholder_syms.values())
    leftover = sympify(rhs_expr - reconstructed)
    if leftover != 0:
        raise ValueError(f"RHS has unexplained leftover term after coefficient extraction: {leftover}")

    return lhs_matrix, rhs_matrix

