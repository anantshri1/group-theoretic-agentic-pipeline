        f"PROBLEM:\n{state['problem']}\n\n"
        f"SOLVER'S FULL RESPONSE (including CLAIM line):\n{state['solution']}\n\n"
        f"SYMBOLIC VERIFIER RESULT:\n{state['verifier_detail']}\n"
