"""# **`LangGraph` Cyclic Loop**

**Architectural Choices**
1. **What actually triggers the Critic?**: When `verified == False` OR `polarity_match == False`.
2. **What does the Critic actually do?**: Feed critique back to Solver for a retry attempt
3. **Loop termination**: If we retry, we need a max-retry count in state to avoid infinite cycles (`LangGraph` conditional edges need an explicit exit condition).

**Block 1**: State Schema and Routing Logic
"""

from typing import Optional
from typing import TypedDict
from langgraph.graph import StateGraph, END


class CriticState(TypedDict):
    problem: str
    solution: str
    claim_tag: Optional[str]
    claim_algebra_N: Optional[int]
    verified: Optional[bool]
    verifier_detail: str
    judge_raw_response: Optional[str]
    polarity_match: Optional[bool]
    critique: Optional[str]      # new: Critic's feedback, fed back to Solver
    retry_count: int             # new: starts at 0, incremented each loop iteration
    max_retries: int             # new: set at invoke time, e.g. 3


def route_after_verifier(state: CriticState) -> str:
    """Conditional edge after the verifier node: loop to critic, or end."""
    if state["verified"] is None:
        # Unparseable claim — not a content disagreement, the Critic can't
        # meaningfully critique a non-claim. Fail loud and stop, per Stage 2's
        # "no silent fallback" convention.
        return END

    needs_retry = (state["verified"] is False) or (state["polarity_match"] is False)

    if needs_retry and state["retry_count"] < state["max_retries"]:
        return "critic"
    return END

