from typing import TypedDict
from langgraph.graph import StateGraph, END


class SolverState(TypedDict):
    problem: str    # the input problem statement (free text)
    solution: str   # the LLM's free-text attempted answer


# The system prompt frames the task domain for the LLM. Kept generic on
# purpose — we're not hand-holding it toward a specific algebra or method,
# since Stage 1's whole point is an *unverified* baseline attempt.
SOLVER_SYSTEM_PROMPT = (
    "You are solving problems in group theory and Lie algebras, "
    "specifically involving su(N) Lie algebra commutation relations "
    "and structure constants. Show your reasoning step by step, then "
    "give a clear final answer."
)


def solver_node(state: SolverState) -> SolverState:
    """Single node: takes a problem statement, returns DeepSeek's attempt."""
    messages = [
        ("system", SOLVER_SYSTEM_PROMPT),
        ("human", state["problem"]),
    ]
    response = llm.invoke(messages)
    return {"problem": state["problem"], "solution": response.content}


